// 6
ALTER SESSION SET CONTAINER = ZAI_PDB;

CREATE TABLESPACE TS_ZAI_PDB
    DATAFILE 'TS_ZAI_PDB.dbf'
    SIZE 7 m
    AUTOEXTEND ON NEXT 5 m
    MAXSIZE 20 m
    EXTENT MANAGEMENT LOCAL;
    
DROP TABLESPACE TS_ZAI_PDB INCLUDING CONTENTS AND DATAFILES;

CREATE TEMPORARY TABLESPACE TS_ZAI_PDB_TEMP
    TEMPFILE 'TS_ZAI_PDB_TEMP.dbf'
    SIZE 5 m
    AUTOEXTEND ON NEXT 3 m
    MAXSIZE 30 m
    EXTENT MANAGEMENT LOCAL;
    
DROP TABLESPACE TS_ZAI_PDB_TEMP INCLUDING CONTENTS AND DATAFILES;

CREATE ROLE RL_ZAIPDB;
GRANT CREATE SESSION,
        CREATE TABLE,
        CREATE TABLESPACE,
        CREATE VIEW,
        CREATE PROCEDURE,
        DROP ANY TABLE,
        DROP TABLESPACE,
        DROP ANY VIEW,
        DROP ANY PROCEDURE TO RL_ZAIPDB;
        
DROP ROLE RL_ZAIPDB;

CREATE PROFILE PF_ZAIPDB LIMIT
    PASSWORD_LIFE_TIME 180
    SESSIONS_PER_USER 3
    FAILED_LOGIN_ATTEMPTS 7
    PASSWORD_LOCK_TIME 1
    PASSWORD_REUSE_TIME 10
    PASSWORD_GRACE_TIME DEFAULT
    CONNECT_TIME 180
    IDLE_TIME 30;

DROP PROFILE PF_ZAIPDB CASCADE;

CREATE USER U1_ZAI_PDB IDENTIFIED BY 12345
    DEFAULT TABLESPACE TS_ZAI_PDB QUOTA UNLIMITED ON TS_ZAI_PDB
    TEMPORARY TABLESPACE TS_ZAI_PDB_TEMP
    PROFILE PF_ZAIPDB
    ACCOUNT UNLOCK;

GRANT RL_ZAIPDB TO U1_ZAI_PDB;

REVOKE RL_ZAIPDB FROM U1_ZAI_PDB;

DROP USER U1_ZAI_PDB;


