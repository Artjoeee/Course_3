CREATE PLUGGABLE DATABASE ZAI_PDB
    ADMIN USER PDB_ADMIN IDENTIFIED BY 12345
    ROLES = (DBA)
    FILE_NAME_CONVERT = ('/opt/oracle/oradata/XE/',
                        '/opt/oracle/oradata/XE/ZAI_PDB/');

ALTER PLUGGABLE DATABASE ZAI_PDB OPEN;


