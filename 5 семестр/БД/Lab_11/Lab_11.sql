SELECT * FROM FACULTY;

CREATE OR REPLACE PROCEDURE GET_TEACHERS_BY_FACULTY(FCODE FACULTY.FACULTY%TYPE)
    IS
    CURSOR TEACHERS_BY_FACULTY IS
    SELECT T.TEACHER_NAME, T.PULPIT FROM TEACHER T 
    JOIN PULPIT P ON T.PULPIT = P.PULPIT
    WHERE P.FACULTY = FCODE;
    
    V_TEACHER_NAME TEACHER.TEACHER_NAME%TYPE;
    V_PULPIT TEACHER.PULPIT%TYPE;
BEGIN
    
    OPEN TEACHERS_BY_FACULTY;
    LOOP
        FETCH TEACHERS_BY_FACULTY INTO V_TEACHER_NAME, V_PULPIT;
        DBMS_OUTPUT.PUT_LINE(V_PULPIT || ' ' || V_TEACHER_NAME);
        EXIT WHEN TEACHERS_BY_FACULTY%NOTFOUND;
    END LOOP;
    CLOSE TEACHERS_BY_FACULTY;
END;
    
BEGIN
    GET_TEACHERS_BY_FACULTY('ИДиП');
END;

DROP PROCEDURE GET_TEACHERS_BY_FACULTY;

