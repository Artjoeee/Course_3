--6. Разработайте пакет TEACHERS, содержащий процедуры и функции:
--GET_TEACHERS (FCODE FACULTY.FACULTY%TYPE)
--GET_SUBJECTS (PCODE SUBJECT.PULPIT%TYPE)
--GET_NUM_TEACHERS (FCODE FACULTY.FACULTY%TYPE) RETURN NUMBER
--GET_NUM_SUBJECTS (PCODE SUBJECT.PULPIT%TYPE) RETURN NUMBER

CREATE OR REPLACE PACKAGE TEACHERS AS
    FCODE FACULTY.FACULTY%TYPE;
    PCODE SUBJECT.PULPIT%TYPE;
    PROCEDURE P_GET_TEACHERS(FCODE FACULTY.FACULTY%TYPE);
    PROCEDURE P_GET_SUBJECTS (PCODE SUBJECT.PULPIT%TYPE);
    FUNCTION P_GET_NUM_TEACHERS(FCODE FACULTY.FACULTY%TYPE) RETURN NUMBER;
    FUNCTION P_GET_NUM_SUBJECTS(PCODE SUBJECT.PULPIT%TYPE) RETURN NUMBER;
END TEACHERS;

CREATE OR REPLACE PACKAGE BODY TEACHERS
IS
    PROCEDURE P_GET_TEACHERS(FCODE FACULTY.FACULTY%TYPE)
    IS
        CURSOR TEACHERS_BY_FACULTY IS
        SELECT T.TEACHER_NAME, T.PULPIT FROM TEACHER T 
        JOIN PULPIT P ON T.PULPIT = P.PULPIT
        WHERE P.FACULTY = FCODE;
        
        V_TEACHER_NAME TEACHER.TEACHER_NAME%TYPE;
        V_PULPIT TEACHER.PULPIT%TYPE;
    BEGIN
        OPEN TEACHERS_BY_FACULTY;
        LOOP
            FETCH TEACHERS_BY_FACULTY INTO V_TEACHER_NAME, V_PULPIT;
            DBMS_OUTPUT.PUT_LINE(V_PULPIT || ' ' || V_TEACHER_NAME);
            EXIT WHEN TEACHERS_BY_FACULTY%NOTFOUND;
        END LOOP;
        CLOSE TEACHERS_BY_FACULTY;
    END P_GET_TEACHERS;

    PROCEDURE P_GET_SUBJECTS(PCODE SUBJECT.PULPIT%TYPE)
    IS
        CURSOR SUBJECTS_BY_PULPIT IS
        SELECT * FROM SUBJECT WHERE PULPIT = PCODE;
        
        V_SUBJECT SUBJECT%ROWTYPE;
    BEGIN
        DBMS_OUTPUT.PUT_LINE('Дисциплины кафедры ' || PCODE);
        FOR V_SUBJECT IN SUBJECTS_BY_PULPIT
        LOOP
            DBMS_OUTPUT.PUT_LINE(V_SUBJECT.SUBJECT || ' ' || V_SUBJECT.PULPIT);
        END LOOP;
    END P_GET_SUBJECTS;
    
    FUNCTION P_GET_NUM_TEACHERS (FCODE FACULTY.FACULTY%TYPE)
    RETURN NUMBER
    IS
        RESULT_NUM NUMBER;
    BEGIN
        SELECT COUNT(TEACHER) INTO RESULT_NUM FROM TEACHER
        WHERE TEACHER.PULPIT IN (SELECT PULPIT FROM PULPIT WHERE FACULTY=FCODE);
        RETURN RESULT_NUM;
    END P_GET_NUM_TEACHERS;
    
    FUNCTION P_GET_NUM_SUBJECTS (PCODE SUBJECT.PULPIT%TYPE) RETURN NUMBER
    IS
        RESULT_NUM NUMBER;
    BEGIN
        SELECT COUNT(SUBJECT) INTO RESULT_NUM FROM SUBJECT WHERE PULPIT=PCODE;
        RETURN RESULT_NUM;
        END P_GET_NUM_SUBJECTS;
END TEACHERS;

BEGIN 
    DBMS_OUTPUT.PUT_LINE('Пакет TEACHERS');
    TEACHERS.P_GET_TEACHERS('ИДиП');
    TEACHERS.P_GET_SUBJECTS('ИСиТ');
    DBMS_OUTPUT.PUT_LINE('Количество преподавателей на факультете: ' || TEACHERS.P_GET_NUM_TEACHERS('ИДиП'));
    DBMS_OUTPUT.PUT_LINE('Количество дисциплин на кафедре: ' || TEACHERS.P_GET_NUM_SUBJECTS('ИСиТ'));
END;