--1. Разработайте локальную процедуру
--GET_TEACHERS (PCODE TEACHER.PULPIT%TYPE)
--Процедура должна выводить список преподавателей из таблицы TEACHER (в
--стандартный серверный вывод), работающих на кафедре заданной кодом в параметре.
--Разработайте анонимный блок и продемонстрируйте выполнение процедуры.

SELECT * FROM TEACHER;

DECLARE
    PROCEDURE GET_TEACHERS(PCODE TEACHER.PULPIT%TYPE)
    IS
        CURSOR PULPIT_TEACHERS IS
        SELECT * FROM TEACHER
        WHERE PULPIT = PCODE;
        V_TEACHER TEACHER%ROWTYPE;
    BEGIN
        FOR V_TEACHER IN PULPIT_TEACHERS 
        LOOP
            DBMS_OUTPUT.PUT_LINE(V_TEACHER.TEACHER || ' ' || V_TEACHER.TEACHER_NAME || ' ' || V_TEACHER.PULPIT);
        END LOOP;
    END;
BEGIN
    GET_TEACHERS('ИСиТ');
END;


